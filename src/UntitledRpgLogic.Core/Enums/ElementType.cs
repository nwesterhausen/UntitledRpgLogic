namespace UntitledRpgLogic.Core.Enums;

/// <summary>
///     Defines fundamental elemental energies or properties.
///     Used for effects directly tied to a basic element.
/// </summary>
public enum ElementType
{
    /// <summary>
    ///     No specific elemental type.
    /// </summary>
    None = 0,

    /// <summary>
    ///     Represents fire energy.
    /// </summary>
    Fire = 1,

    /// <summary>
    ///     Represents electrical energy.
    /// </summary>
    Lightning = 2,

    /// <summary>
    ///     Represents water energy.
    /// </summary>
    Water = 3,

    /// <summary>
    ///     Represents earth energy.
    /// </summary>
    Earth = 4,

    /// <summary>
    ///     Represents air energy.
    /// </summary>
    Air = 5,

    /// <summary>
    ///     Represents ice energy.
    /// </summary>
    Ice = 6,

    /// <summary>
    ///     Represents raw magical force.
    /// </summary>
    Force = 7,

    /// <summary>
    ///     Represents light energy.
    /// </summary>
    Light = 8,

    /// <summary>
    ///     Represents dark energy.
    /// </summary>
    Dark = 9,

    /// <summary>
    ///     Represents raw, unaligned magical energy.
    /// </summary>
    Arcane = 10
}
